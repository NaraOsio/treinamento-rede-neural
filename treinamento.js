let pesos = Array.from({length: 10}, () => new Array(70).fill(-1));

// outras variaveis
let ajustes, sum, y, quantidadeAjustes = 0, quantidadeRepeticoes = 0;

do {
    ajustes = 0;

    for (let index = 0; index < treinamento.length; index++) {
        const objeto = treinamento[index];

        for (let digito = 0; digito < 10; digito++) {
            sum = soma(objeto.entradas, pesos[digito]);
            y = transferencia(sum);

            if (y != objeto.saidaEsperada[digito]) {
                ajuste(objeto.entradas, objeto.saidaEsperada[digito], y, pesos[digito]);
                ajustes++;
                quantidadeAjustes++;
            }
        }
    }

    quantidadeRepeticoes++;
} while (ajustes != 0)

function ajuste(entradas, saidaDesejada, saidaObtida, pesosDoDigito) {
    for (let index = 0; index < entradas.length; index++) {
        pesosDoDigito[index] = pesosDoDigito[index] + 1 * (saidaDesejada - saidaObtida) * entradas[index];
    }
}

function soma(entradas, pesosDoDigito) {
    let soma = 0;
    for (let index = 0; index < entradas.length; index++) {
        soma += entradas[index] * pesosDoDigito[index];
    }
    return soma;
}

function transferencia(soma) {
    if (soma <= 0) {
        return 0;
    }
    return 1;
}

