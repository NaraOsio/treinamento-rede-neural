// ── função para reconhecer um digito ────────────────────────
function reconhecerDigito(entradas) {
    let melhorDigito = -1;
    let melhorSoma = -Infinity;

    for (let digito = 0; digito < 10; digito++) {
        sum = soma(entradas, pesos[digito]);
        y = transferencia(sum);

        if (y === 1) {
            if (sum > melhorSoma) {
                melhorSoma = sum;
                melhorDigito = digito;
            }
        }
    }

    // fallback: nenhum perceptron disparou — pega o de maior soma
    if (melhorDigito === -1) {
        for (let digito = 0; digito < 10; digito++) {
            sum = soma(entradas, pesos[digito]);
            if (sum > melhorSoma) {
                melhorSoma = sum;
                melhorDigito = digito;
            }
        }
    }

    return melhorDigito;
}


